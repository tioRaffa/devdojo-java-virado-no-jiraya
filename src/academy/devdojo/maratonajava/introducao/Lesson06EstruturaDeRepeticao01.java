package academy.devdojo.maratonajava.introducao;

public class Lesson06EstruturaDeRepeticao01 {
    public static void main(String[] args) {
        // while e for
        int count = 0;
        //while
        while (count<10){
            System.out.println(++count);
            //count++;
        }
        //for      V     C  I      V= Variavel, C= Condição e I= Incremento
        for (int i=0; i<=5; i++){
            System.out.println(i);
        }

    }
}
